public class MyProfileData {
    private int profile_image;
    private String group_number, jungmo_number, profile_name;

    public int getProfile_image() {
        return profile_image;
    }

    public void setProfile_image(int profile_image) {
        this.profile_image = profile_image;
    }

    public String getGroup_number() {
        return group_number;
    }

    public void setGroup_number(String group_number) {
        this.group_number = group_number;
    }

    public String getJungmo_number() {
        return jungmo_number;
    }

    public void setJungmo_number(String jungmo_number) {
        this.jungmo_number = jungmo_number;
    }

    public String getProfile_name() {
        return profile_name;
    }

    public void setProfile_name(String profile_name) {
        this.profile_name = profile_name;
    }
}
