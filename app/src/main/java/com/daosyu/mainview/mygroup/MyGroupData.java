public class MyGroupData {
    private int profile_image;
    private String small_group_name, small_group_title, group_people_number;

    public int getProfile_image() {
        return profile_image;
    }

    public void setProfile_image(int profile_image) {
        this.profile_image = profile_image;
    }

    public String getSmall_group_name() {
        return small_group_name;
    }

    public void setSmall_group_name(String small_group_name) {
        this.small_group_name = small_group_name;
    }

    public String getSmall_group_title() {
        return small_group_title;
    }

    public void setSmall_group_title(String small_group_title) {
        this.small_group_title = small_group_title;
    }

    public String getGroup_people_number() {
        return group_people_number;
    }

    public void setGroup_people_number(String group_people_number) {
        this.group_people_number = group_people_number;
    }
}
